package it.polito.dp2.NFV.sol1;

public class NfvInfoSerializerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1660750033754099811L;

	public NfvInfoSerializerException() {
		super();
	}

	public NfvInfoSerializerException(String message) {
		super(message);
	}

	public NfvInfoSerializerException(String message, Throwable cause) {
		super(message, cause);
	}

	public NfvInfoSerializerException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NfvInfoSerializerException(Throwable cause) {
		super(cause);
	}

}
